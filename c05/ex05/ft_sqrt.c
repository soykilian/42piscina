int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	n = 0;
	i = 1;
	if (nb < 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (i < nb)
	{
		n = i * i;
		if (n == nb)
			return (1);
		else if (n > nb)
			break ;
		i++;
	}
	return (0);
}
