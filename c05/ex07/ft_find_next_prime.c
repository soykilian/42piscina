int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	while (nb / i > nb % i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	n;

	n = 0;
	while (!n)
	{
		n = ft_is_prime(nb);
		if (n)
			return (nb);
		nb++;
	}
	return (nb);
}
