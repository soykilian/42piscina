#include <unistd.h>

int	main(int args, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = args - 1;
	if (args < 2)
		return (0);
	while (j > 0)
	{
		while (argv[j][i] != 0)
		{
			write(1, &argv[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		j--;
	}
}
